void	ft_sort_int_tab(int *tab, int size)
{
	int	save;
	int	a;

	a = 0;
	while (a < (size - 1))
	{
		if (tab[a] > tab[a + 1])
		{
			save = tab[a];
			tab[a] = tab[a + 1];
			tab[a + 1] = save;
			a = -1;
		}
		a++;
	}
}
