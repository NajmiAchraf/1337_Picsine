void	ft_rev_int_tab(int *tab, int size)
{
	int	inv;
	int	a;
	int	b;

	a = 0;
	b = size - 1;
	while (a < (size / 2))
	{
		inv = tab[a];
		tab[a] = tab[b];
		tab[b] = inv;
		a++;
		b--;
	}
}
