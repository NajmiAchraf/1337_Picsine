#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (0 <= nb && nb < 10)
	{
		print(nb + '0');
	}
	else if (10 <= nb && nb <= 2147483647)
	{
		ft_putnbr(nb / 10);
		print(nb % 10 + '0');
		if (-2147483648 < nb && nb < 0)
			print('-');
	}
	else if (nb == -2147483648)
		write(1, "-2147483648", 11);
	else if (-2147483648 < nb && nb < 0)
	{
		print('-');
		ft_putnbr(nb * -1);
	}
}
